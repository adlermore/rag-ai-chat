// Обратный индекс: нормализованная фраза → интент (O(1) поиск).
const INDEX = new Map<string, SmallTalkIntent>();
for (const intent of Object.keys(PHRASES) as SmallTalkIntent[]) {
  for (const phrase of PHRASES[intent]) INDEX.set(phrase, intent);
}

/**
 * Нормализация: нижний регистр, снятие армянских внутрисловных знаков
 * (՞ ՜ ՛ ՚), замена разделителей (։ ՝ и латинская пунктуация) и эмодзи на
 * пробелы, схлопывание пробелов.
 */
export function normalize(raw: string): string {
  return raw
    .toLowerCase()
    .normalize("NFC")
    .replace(/[՚՛՜՞]/g, "")
    .replace(/[։՝.,:;!?()"'«»/\\\-–—]/g, " ")
    .replace(
      /[\u{1F000}-\u{1FAFF}\u{2600}-\u{27BF}\u{FE00}-\u{FE0F}\u{1F1E6}-\u{1F1FF}]/gu,
      " ",
    )
    .replace(/\s+/g, " ")
    .trim();
}

/**
 * Возвращает разговорный интент и готовый ответ, если сообщение — явная
 * социальная реплика; иначе null (сообщение идёт в обычный RAG-пайплайн).
 */
export function detectSmallTalk(raw: string): SmallTalk | null {
  const normalized = normalize(raw);
  if (!normalized) return null;
  const intent = INDEX.get(normalized);
  return intent ? { intent, reply: REPLIES[intent] } : null;
}
