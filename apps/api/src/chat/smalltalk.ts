/**
 * Разговорные интенты (приветствие/благодарность/прощание/«что умеешь»).
 *
 * Такие реплики — НЕ вопрос к базе знаний, поэтому прогонять их через
 * hybrid search + guardrail бессмысленно: пользователь получает холодный
 * «ответ не найден». Здесь — быстрый детерминированный роутер (regex/словарь,
 * без LLM → 0 токенов и ~0 задержки), который перехватывает ТОЛЬКО явные
 * социальные реплики и отвечает тёпло и профессионально по-армянски.
 *
 * Важно: фактологический guardrail этим не ослабляется. Детектор срабатывает
 * лишь на точное совпадение короткой фразы; всё остальное (в т.ч. «привет, а
 * сколько дней отпуска?») уходит в обычный RAG-путь с порогами.
 */

export type SmallTalkIntent = "greeting" | "thanks" | "goodbye" | "capability";

